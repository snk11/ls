#include "ft_ls.h"

void		ft_putday(char *s)
{
	int		cnt;

	cnt = 0;
	while (*s && cnt < 2)
	{
		while (*s && *s != ' ')
			s++;
		while (*s && *s == ' ')
			s++;
		cnt++;
	}
	while (*s && *s != ' ')
	{
		write(1, s, 1);
		s++;
	}
}
