# include "ft_ls.h"

int		ft_islnk(char *s)
{
	struct stat		sb;

	if(lstat(s, &sb) == 0)
	{
		if(S_ISLNK(sb.st_mode))
			return (1);
	}
	else
		return (-1);
	return (0);

}
