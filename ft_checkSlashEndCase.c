#include "ft_ls.h"

int		ft_checkSlashEndCase(char *s)
{
	int		i;

	i = 0;
	while(s[i])
	{
		if (s[i] == '/' && !s[i + 1])
			return (1);
		i++;
	}
	return (0);
}
