#include "ft_ls.h"

int	ft_check_string(char *s)
{
	if (ft_strcmp(s, "-1") != 0 && ft_strcmp(s, "--") != 0
			&& ft_strcmp(s, "-1R") != 0 && ft_strcmp(s, "-1a") != 0
			&& ft_strcmp(s, "-1r") != 0 && ft_strcmp(s, "-1t") != 0
			&& ft_strcmp(s, "-l") != 0)
		return (1);
	return (0);
}
