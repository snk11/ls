#include "ft_ls.h"

int			ft_countelem(t_lsto *lst1)
{
	int		cnt2;

	cnt2 = 0;
	while(lst1)
	{
		lst1 = lst1->next;
		cnt2++;
	}
	return (cnt2);
}
